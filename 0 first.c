// pre processor directive (runs before the compiler)
/* multiple line comment */
#include <stdio.h>

int main()
{
    int a = 1;
    float b = 8.5;
    char c = 'a';
    printf("the value of a is %d \n", a);
    printf("the value of a is %f \n", b);
    printf("the value of a is %c \n", c);
    printf("the value of a is %f \n", a+b);

    return 0;
}