// Print numbers 1-5
#include <stdio.h>

int main()
{
    int a;
    printf("%d\n", a);
    a++;
    printf("%d\n", a);
    a++;
    printf("%d\n", a);
    a++;
    printf("%d\n", a);
    a++;
    printf("%d\n", a);
    a++;
    printf("%d\n", a);

    return 0;
}
